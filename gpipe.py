axes[1,1].legend(frameon=True, fontsize=12)

plt.suptitle(f"GPipe Performance Analysis - ResNet-101 (BS={BATCH_SIZE}, Micro-batches={MICRO_BATCHES})", fontsize=18, fontweight='bold', y=1.02)
plt.tight_layout()

